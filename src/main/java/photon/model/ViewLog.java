package photon.model;

public class ViewLog {
    private Integer vlid;
    private Integer cid;
    private int count;

    public Integer getVlid() {
        return vlid;
    }

    public void setVlid(Integer vlid) {
        this.vlid = vlid;
    }

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
